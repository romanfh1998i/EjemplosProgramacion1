#include "operaciones.h"
#include <math.h>
float FuncionSuma(float a, float b)
{
  return a + b;
}

float  FuncionResta(float a, float b){
  return a - b ;

}
float FuncionMultiplica(float a, float b ){


  return a * b;
}
float FuncionDivide(float a, float b){
  return a / b ;
}

float FuncionRaizCuadrada1(float a){
    
    return sqrt(a);
}
float FuncionRaizCuadrada2(float b){
    
    return sqrt(b);
}

float FuncionPotencia(float a,float b){
    return pow(a,b);
}